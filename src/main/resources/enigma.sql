CREATE TABLE CERTIFICATES (ID_CERT INTEGER PRIMARY KEY, CERTNAME VARCHAR(200),CN VARCHAR(200),ALGO VARCHAR(64),CERTFILE BLOB,SHA256  VARCHAR(256),THUMBPRINT  VARCHAR(256),ID_ISSUER_CERT INTEGER, ID_PRIVATEKEY INTEGER, CERTTYPE INTEGER, EXPIRYDATE DATE, SERIAL VARCHAR(10), ACSERIALCURSOR VARCHAR(10), CRLLASTUPDATE DATE, STATUS VARCHAR(12));
CREATE SEQUENCE CERTIFICATES_SEQ START WITH 1 INCREMENT BY 1;
CREATE TABLE X509KEYS (ID_KEY INTEGER PRIMARY KEY,	KEYNAME VARCHAR(200), KEYTYPE INTEGER,KEYFILE BLOB, ALGO VARCHAR(64), SHA256  VARCHAR(256),ID_ASSOCIATED_KEY INTEGER);
CREATE SEQUENCE X509KEYS_SEQ START WITH 1 INCREMENT BY 1;
CREATE TABLE PGPKEYS (ID_KEY INTEGER PRIMARY KEY, KEYNAME VARCHAR(64),KEYTYPE INTEGER,ALGO VARCHAR(64),SHA256  VARCHAR(256), ID_PARENT_KEY INTEGER);
CREATE SEQUENCE PGPKEYS_SEQ START WITH 1 INCREMENT BY 1;
CREATE TABLE CRLS (ID_CRL INTEGER PRIMARY KEY, CRLFILE BLOB,ID_CA_CERT INTEGER, DAYCYCLE INTEGER, STARTDATE DATE, ENDDATE DATE);
CREATE SEQUENCE CRLS_SEQ START WITH 1 INCREMENT BY 1;

